idolList.forEach((idol) => {
  const card = document.createElement("div");
  card.className = "idol-list-card";

  const avatar = document.createElement("img");
  avatar.src = idol.avatar;
  avatar.className = "idol-avatar";
  avatar.style.borderColor = idol.borderColor;

  const info = document.createElement("div");
  info.className = "idol-info";

  const name = document.createElement("div");
  name.className = "idol-name";
  name.textContent = idol.name;

  const divider = document.createElement("div");
  divider.className = "divider";

  const stats = document.createElement("div");
  stats.className = "idol-stats";
  stats.innerHTML = `
    <span>5⭐: ${idol.stars.five}</span>
    <span>4⭐: ${idol.stars.four}</span>
    <span>3⭐: ${idol.stars.three}</span>
  `;

  info.appendChild(name);
  info.appendChild(divider);
  info.appendChild(stats);

  card.appendChild(avatar);
  card.appendChild(info);

  listContainer.appendChild(card);
});
